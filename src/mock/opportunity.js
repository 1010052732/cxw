export function buildComboReport(ids, options = {}) {
  const weights = options.weights || DEFAULT_WEIGHTS
  let items = ids.map((oppId, i) => buildEvaluationReport(oppId, { ...options, weights, rank: i + 1 }))
  items = items.sort((a, b) => b.compositeScore - a.compositeScore).map((item, i) => ({ ...item, rank: i + 1 }))

  return {
    reportNo: `RPT-${new Date().toISOString().slice(0, 10).replace(/-/g, '')}-${String(items.length).padStart(2, '0')}`,
    type: 'combo',
    generatedAt: new Date().toLocaleString('zh-CN', { hour12: false }),
    scheme: options.scheme || '商机评估方案 V1',
    count: items.length,
    items,
    weights,
    summary: items.length
      ? `本组合报告涵盖 ${items.length} 个商机。综合排序首位为「${items[0].title}」（${items[0].compositeScore}分），末位为「${items[items.length - 1].title}」（${items[items.length - 1].compositeScore}分）。建议优先配置资源跟进排名前 3 的商机。`
      : '暂无商机数据',
  }
}

